package basics;

public class DataTypes {
    public static void main(String[] args) {
        //Integer Data type
        byte num1 = 100;
        short num2 = 1000;
        int num3 = 3425354;
        long num4 = 4134253453L;

        //Floating point data type
        float num5 = 12312.243f;
        double num6 = 24234.2324324586522844d;

        //Boolean data type
        boolean redLightSwitch = false;
        boolean blackLightSwitch = true;

        //Character data type
        char char1 = '%';
        char char2 = 'y';
        char char3 = '0';
        char char4 = '\n';

        //String data type
        String text1 = "This is very good text";

        //Print out variable values
        System.out.println(num1);
        System.out.println(num6);
        System.out.println(redLightSwitch);
        System.out.println(blackLightSwitch);
        System.out.println(char4);
        System.out.println(char2);
        System.out.println(text1);
        

    }
}
